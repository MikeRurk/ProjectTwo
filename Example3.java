class Example3 {
    public static void main(String args[]){
        int var1;
        double var2;

        var1 = 10;
        var2 = 10;

        System.out.println("Переменная var1 содержит: "+var1);
        System.out.println("Переменная var2 содержит: "+var2);
        System.out.println();

        var1 = var1 / 4;
        var2 = var2 / 4;

        System.out.println("После деления var1 содержит: "+var1);
        System.out.println("После деления var2 содержит: "+var2);
    }
}
